#include "../libft.h"

int	ft_intlen(int n)
{
	int	cpt;

	cpt = 1;
	if (n == -2147483648)
	{
		cpt = 11;
		return (cpt);
	}
	if (n < 0)
	{
		cpt++;
		n *= -1;
	}
	while (n > 9)
	{
		cpt++;
		n /= 10;
	}
	return (cpt);
}
