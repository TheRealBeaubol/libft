#include "libft.h"

char	*ft_str_lower(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i++] += 32;
		else
			i++;
	}
	return (str);
}
