#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t					i;
	const unsigned char		*str;

	str = s;
	i = 0;
	if (!n)
		return (NULL);
	while (i < n && *(str + i) != (unsigned char)c)
		i++;
	if (i >= n && (unsigned char)c != '\0')
		return (NULL);
	return ((unsigned char *)str + i);
}
